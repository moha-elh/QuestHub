import 'dart:io';

import 'package:flutter_test/flutter_test.dart';
import 'package:questhub/features/proof/data/image_compressor.dart';

void main() {
  group('DefaultImageCompressor', () {
    late DefaultImageCompressor compressor;

    setUp(() {
      compressor = const DefaultImageCompressor();
    });

    test('compress returns a File', () async {
      // Create a small dummy image to simulate a real file.
      final input = await _createDummyImage();

      final result = await compressor.compress(input);

      expect(result, isA<File>());
      expect(result.path, contains('.jpg'));
    });
  });
}

